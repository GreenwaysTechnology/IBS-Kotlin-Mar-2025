package com.ibs.kotlin.basics.types

fun main() {
    var speed = 100
    println(speed)
  //  speed = true
    var a = 1
    var s = 12
    var b = 10
    var l = 19999
    var f = 10.3f
    var d = 90.334
    var isActive = true
    println("Int " + a)
    println("Short " + s.toShort())
    println("Byte " + b.toByte())
    println("Long " + l.toLong())
    println("Float " + f.toFloat())
    println("Double " + d)
    println("Boolean " + isActive)
}